                return [$case->value => "<div class=\"flex items-center gap-2\">{$iconElement}<span>{$case->getLabel()}</span></div>"];
            })
            ->toArray();
    }

    /**
     * @return array<int, self>
     */
    private static function validCases(): array
    {
        return collect(self::cases())
            ->filter(function (self $case): bool {
                try {
                    svg($case->value);

                    return true;
                } catch (\Throwable) {
                    return false;
                }
            })
            ->values()
            ->toArray();
    }
